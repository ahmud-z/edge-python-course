# Write a Python Program to Display Min and Max Value from a Dictionary Value.

data = {
    "n1": 12,
    "n2": 112,
    "n3": 4,
    "n4": 456,
    "n5": 24,
}

print("\nMin Value of Given Dictionary:")
print(f"{min(data.values())}")

print("\nMax Value of Given Dictionary:")
print(f"{max(data.values())}")
