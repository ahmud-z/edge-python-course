# Write a Python Program to get a Sentence from User to Count Total Number of Characters in a Sentence


def calc_circle_area(radius):
    pi = 3.1416
    return pi * (radius * radius)


def calc_square_area(side):
    return side * side


def calc_rectangle_area(length, width):
    return length * width


print(f"Area of given circle: {calc_circle_area(10)}")
print(f"Area of given square: {calc_square_area(2)}")
print(f"Area of given rectangle: {calc_rectangle_area(10, 20)}")
