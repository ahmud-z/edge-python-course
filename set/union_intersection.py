set1 = {1, 4, 5, 4, 1, 8, 1, 5, 3, 8}
set2 = {3, 2, 5, 6, 3, 4, 3, 7, 4, 1}

print(f"set1 = {set1}")
print(f"set2 = {set2}")

set_union = set1.union(set2)
print("\nResult of Set Union: ")
print(set_union)

set_intersection = set1.intersection(set2)
print("\nResult of Set Intersection: ")
print(set_intersection)
